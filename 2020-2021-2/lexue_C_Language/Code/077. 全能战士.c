/*
全排有n名战士，其中会骑马的有n1人，会摔跤的有n2人，会拳击的有n3人。还知道不少人是多面手，其中会骑马和摔跤的有n12人，会摔跤和拳击的有n23人，会拳击和骑马的有n13，还有若干三项全会。编程，输出三项全能的有几人？
例如：输入：n=50, n1=30, n2=28, n3=20, n12=14，n23=12, n31 = 8。
         输出：6<回车> 
*/
